export function getProblems() {
  return problemBank.map((problem) => ({
    reviewStatus: "vetted",
    reviewLabel: "준비 완료",
    ...problem,
  }));
}

export const elementaryMathConcepts = new Set([
  "fraction_common_denominator",
  "ratio_part_total",
]);

export function isElementaryJudgeProblem(problem) {
  return problem?.subject === "math" && elementaryMathConcepts.has(problem.concept);
}

export function getElementaryMathProblems(problems = getProblems()) {
  return problems.filter(isElementaryJudgeProblem);
}

export function getDefaultJudgeProblems() {
  return getElementaryMathProblems(getProblems());
}

export const defaultMisconceptionByConcept = {
  fraction_common_denominator: "fraction_denominator_direct_add",
  ratio_part_total: "ratio_total_parts_confusion",
};

export function getDefaultMisconceptionForConcept(conceptId = "") {
  return defaultMisconceptionByConcept[conceptId] || `${conceptId || "unknown"}_concept_misunderstanding`;
}

export function buildProblemConceptMap(problems = getDefaultJudgeProblems()) {
  const normalizedProblems = problems.map((problem) => ({
    reviewStatus: "vetted",
    reviewLabel: "준비 완료",
    ...problem,
  }));
  const groupedByConcept = normalizedProblems.reduce((groups, problem) => {
    const conceptId = problem.concept || "unknown";
    if (!groups.has(conceptId)) {
      groups.set(conceptId, {
        conceptId,
        conceptKo: problem.conceptKo || "",
        problemIds: [],
        vettedProblemIds: [],
        generatedProblemIds: [],
      });
    }

    const group = groups.get(conceptId);
    group.problemIds.push(problem.id);
    if ((problem.reviewStatus || "vetted") === "vetted" && !problem.generatedBy) {
      group.vettedProblemIds.push(problem.id);
    }
    if (problem.generatedBy || (problem.reviewStatus || "vetted") !== "vetted") {
      group.generatedProblemIds.push(problem.id);
    }

    return groups;
  }, new Map());

  const rows = normalizedProblems.map((problem) => {
    const conceptId = problem.concept || "unknown";
    const group = groupedByConcept.get(conceptId);
    const sameConceptRetryProblemIds = (group?.problemIds || []).filter((problemId) => problemId !== problem.id);
    const reviewStatus = problem.reviewStatus || "vetted";
    const generatedBy = problem.generatedBy || "";

    return {
      problemId: problem.id,
      subject: problem.subject,
      subjectKo: problem.subjectKo,
      level: problem.level,
      conceptId,
      conceptKo: problem.conceptKo,
      misconceptionId: getDefaultMisconceptionForConcept(conceptId),
      sameConceptRetryProblemIds,
      hasSameConceptRetry: sameConceptRetryProblemIds.length > 0,
      reviewStatus,
      generatedBy,
      sourceType: reviewStatus === "vetted" && !generatedBy ? "vetted_source" : "candidate_requires_review",
    };
  });

  const conceptGroups = [...groupedByConcept.values()]
    .map((group) => ({
      conceptId: group.conceptId,
      conceptKo: group.conceptKo,
      problemCount: group.problemIds.length,
      vettedProblemCount: group.vettedProblemIds.length,
      generatedProblemCount: group.generatedProblemIds.length,
      problemIds: group.problemIds,
    }))
    .sort((a, b) => a.conceptId.localeCompare(b.conceptId));

  const issues = [];
  for (const row of rows) {
    if (!row.problemId) issues.push("missing problemId");
    if (!row.conceptId || row.conceptId === "unknown") issues.push(`${row.problemId}: missing conceptId`);
    if (!row.hasSameConceptRetry) issues.push(`${row.problemId}: no same-concept retry problem`);
    if (row.reviewStatus !== "vetted") issues.push(`${row.problemId}: not vetted`);
    if (row.generatedBy) issues.push(`${row.problemId}: generated problem cannot be judge-source evidence`);
  }

  return {
    status: issues.length ? "FAIL" : "PASS",
    researchPattern: "problem_skill_retry_map",
    rowCount: rows.length,
    conceptCount: conceptGroups.length,
    conceptGroups,
    rows,
    issueCount: issues.length,
    issues,
  };
}

export function validateProblemBank(problems = problemBank) {
  const ids = new Set();
  const issues = [];

  problems.forEach((problem, index) => {
    const label = problem?.id || `index:${index}`;
    if (!problem?.id) issues.push(`${label}: missing id`);
    if (ids.has(problem.id)) issues.push(`${label}: duplicate id`);
    ids.add(problem.id);

    for (const field of ["subject", "subjectKo", "concept", "conceptKo", "level", "question", "explanation"]) {
      if (!problem?.[field] || typeof problem[field] !== "string") {
        issues.push(`${label}: missing ${field}`);
      }
    }

    if (!Array.isArray(problem?.options) || problem.options.length < 2) {
      issues.push(`${label}: options must contain at least two choices`);
    } else if (!Number.isInteger(problem.answer) || problem.answer < 0 || problem.answer >= problem.options.length) {
      issues.push(`${label}: answer index out of options range`);
    }
  });

  return {
    status: issues.length === 0 ? "PASS" : "FAIL",
    issueCount: issues.length,
    issues,
  };
}

export function summarizeProblemBank(problems = getProblems()) {
  const validation = validateProblemBank(problems);
  const subjectMap = new Map();
  const conceptMap = new Map();
  let vettedCount = 0;

  problems.forEach((problem) => {
    if ((problem.reviewStatus || "vetted") === "vetted") vettedCount += 1;

    if (!subjectMap.has(problem.subject)) {
      subjectMap.set(problem.subject, {
        subject: problem.subject,
        subjectKo: problem.subjectKo,
        count: 0,
      });
    }
    subjectMap.get(problem.subject).count += 1;

    conceptMap.set(problem.concept, (conceptMap.get(problem.concept) || 0) + 1);
  });

  const subjects = [...subjectMap.values()].sort((a, b) => a.subject.localeCompare(b.subject));
  const repeatedConceptGroups = [...conceptMap.values()].filter((count) => count >= 2).length;

  return {
    status: validation.status,
    problemCount: problems.length,
    vettedCount,
    subjectCount: subjects.length,
    subjects,
    conceptCount: conceptMap.size,
    repeatedConceptGroups,
    issueCount: validation.issueCount,
    issues: validation.issues,
    summaryText: `${problems.length}개 준비된 문항, ${subjects.length}과목, ${repeatedConceptGroups}개 반복 개념 그룹`,
  };
}

export function getProblemsByConcept(concept, problems = problemBank) {
  return problems
    .map((problem) => ({
      reviewStatus: problem.reviewStatus || "vetted",
      reviewLabel: problem.reviewLabel || "준비 완료",
      ...problem,
    }))
    .filter((problem) => problem.concept === concept);
}

